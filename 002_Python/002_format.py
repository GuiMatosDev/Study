# .format - Aloca a variavél na posição desejada do texto
sinal = '+'
operacao = 'soma'
print('A {} de 1 {} 1 é igual a 2.'.format(operacao, sinal))

# Com .format é possível truncar digitos de um numero flutuante
# Em programação se utiliza ponto no lugar de virgula
pi = 3.141516
print('Pi equivale a {:.2f}'.format(pi))

